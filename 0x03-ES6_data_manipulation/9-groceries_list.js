/**
 * Creates a Map of grocery items and their quantities.
 * @returns {Map} A Map where the key is the item name and the value is the quantity.
 */
export default function groceriesList() {
  return new Map([
    ['Apples', 10],
    ['Tomatoes', 10],
    ['Pasta', 1],
    ['Rice', 1],
    ['Banana', 5],
  ]);
}
